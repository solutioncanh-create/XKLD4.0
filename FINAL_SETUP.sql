DROP POLICY IF EXISTS "Enable all access for all users" ON public.yeu_cau_tu_van;
CREATE POLICY "Enable all access for all users" ON public.yeu_cau_tu_van FOR ALL USING (true) WITH CHECK (true);

-- candidate_order
DROP POLICY IF EXISTS "Enable all access for all users" ON public.candidate_order;
CREATE POLICY "Enable all access for all users" ON public.candidate_order FOR ALL USING (true) WITH CHECK (true);
